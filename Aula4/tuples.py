## TUPLAS EM PYTHON

# Declarando uma tupla
tupla_01 = (1 ,12 ,True , "Olá mundo", 1.5, False)
tupla_02 = 1 ,12 ,True , "Olá mundo", 1.5, False

print(type(tupla_01)) # Verificando tipo das tuplas
print(type(tupla_01)) 

# Usando metodo count
print(tupla_01.count(1)) # Quantidade de elementos

# Usando o metodo index
print(tupla_01.index(12)) # Qual indice esta localizado o elemento na tupla

# Verificar a existencia de um elemento na tupla
print(13 in tupla_01) 

