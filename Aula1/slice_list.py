## SLICES IN PYTHON

lista_simples_inteiro = [1, 2, 5, 12, 56, 98] 

## Utilizando slices
print(lista_simples_inteiro[0:4]) # Retorna os 4 primeiros elementos;
print(lista_simples_inteiro[1:]) # Retorna os elementos a partir da posição 2;
print(lista_simples_inteiro[:3]) # Retorna os elementos 1 até o elemento 4;

nova_lista = lista_simples_inteiro[1:5] # Salvando resultado em outra lista;
print(nova_lista)