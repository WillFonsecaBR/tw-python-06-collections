# TRABALHANDO COM RANGES EM PYTHON

# O range como a tupla ele é imutavel

range_1 = range(5)  # Vai de 0 a 4.
range_2 = range(2, 10) # vai de 2 até 9.
range_3 = range(2, 10, 2) # vai de 2 até 10 de 2 em dois.

print("===")
for i in range_1:
    print(i)
print()

print("===")
for i in range_2:
    print(i)
print()

print("===")
for i in range_3:
    print(i)
print()
